package Clusterers;
/**
 * Ant class acts as a container to hold a set of antID numbers, their positions, and their paths
 */

public class Ant {
    boolean isCarrying;
    int antID;
    int x;
    int y;

    public Ant(int x, int y) {
        this.antID = antID;
        this.x = x;
        this.y = y;
    }

}
